    private void cashInsertKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cashInsertKeyTyped
        char c = evt.getKeyChar();
        if (!Character.isDigit(c)) {
            evt.consume();
        }
    }//GEN-LAST:event_cashInsertKeyTyped

    private void submitCashButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitCashButtonActionPerformed
        Ctm.cashIns = Integer.parseInt(cashInsert.getText());
        if (Ctm.cashIns < (Ctm.total)) {
            warningLab.setText("Enter a value more than Required Cash");
        } else {

            Ctm.cashRet = (Ctm.cashIns) - (Ctm.total);
            cashReturnVal.setText(String.valueOf(Ctm.cashRet));
        }
    }//GEN-LAST:event_submitCashButtonActionPerformed

