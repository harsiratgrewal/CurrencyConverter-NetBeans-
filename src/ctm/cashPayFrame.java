    private void continueButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_continueButtonActionPerformed
        this.setVisible(false);
        Ctm.transSum.currConvVal.setText(String.valueOf(Ctm.usdAmount));
        Ctm.transSum.excRateVal.setText(String.valueOf(Ctm.excRate));
        Ctm.transSum.initialAmntVal.setText(String.valueOf(Ctm.initialAmount));
        Ctm.transSum.feesVal.setText(String.valueOf(Ctm.fees));
        Ctm.transSum.totalVal.setText(String.valueOf(Ctm.total));
        Ctm.transSum.payMeth.setText(Ctm.payMeth);
        Ctm.transSum.payMeth1.setText("Cash Returned");
        Ctm.transSum.payMeth1Val.setText(String.valueOf(Ctm.cashRet));
        int Rn = 0;
        for (int i = 0; i < 100; i++) {
            Rn = r.nextInt(10) + 1;
        }
        Ctm.settranID(Rn);
        Ctm.transSum.transIdVal.setText(String.valueOf(Rn));
        Ctm.transSum.setVisible(true);

    }//GEN-LAST:event_continueButtonActionPerformed

